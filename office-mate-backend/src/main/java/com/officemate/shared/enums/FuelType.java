package com.officemate.shared.enums;

/**
 * Enum representing the fuel types for vehicles in the ride-sharing system.
 * Used for driver vehicle specifications and environmental tracking.
 */
public enum FuelType {
    /**
     * Petrol/Gasoline powered vehicle
     */
    PETROL,
    
    /**
     * Diesel powered vehicle
     */
    DIESEL,
    
    /**
     * Electric powered vehicle (battery)
     */
    ELECTRIC,
    
    /**
     * Hybrid vehicle (combination of fuel and electric)
     */
    HYBRID,
    
    /**
     * Compressed Natural Gas powered vehicle
     */
    CNG
}
