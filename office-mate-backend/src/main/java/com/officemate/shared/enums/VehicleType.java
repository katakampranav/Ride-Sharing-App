package com.officemate.shared.enums;

/**
 * Enum representing the types of vehicles supported in the ride-sharing system.
 * Used for driver vehicle classification and rider preferences.
 */
public enum VehicleType {
    /**
     * Four-wheeled passenger vehicle (capacity: 1-7)
     */
    CAR,
    
    /**
     * Two-wheeled motorized vehicle (capacity: 1-2)
     */
    MOTORCYCLE,
    
    /**
     * Two-wheeled motorized scooter (capacity: 1-2)
     */
    SCOOTER,
    
    /**
     * Two-wheeled non-motorized vehicle (capacity: 1-2)
     */
    BICYCLE
}
