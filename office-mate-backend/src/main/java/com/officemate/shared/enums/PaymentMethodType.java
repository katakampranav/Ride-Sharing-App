package com.officemate.shared.enums;

/**
 * Enum representing payment method types supported by the wallet system.
 */
public enum PaymentMethodType {
    /**
     * Credit card payment method
     */
    CREDIT_CARD,
    
    /**
     * Bank account payment method
     */
    BANK_ACCOUNT,
    
    /**
     * UPI (Unified Payments Interface) payment method
     */
    UPI
}
